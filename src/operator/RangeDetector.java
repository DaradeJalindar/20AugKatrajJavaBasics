package operator;

public class RangeDetector {

	public static void main(String[] args) {
		
		
		int a =26;
		
		if(a>=0 && a<=20)
		{
			System.out.println(a +" is between 0 to 20");
		}
		
		if(a>20 && a<41 )
		{
			System.out.println(a +" is between 21 to 40");
		}
		
		if(a>40 && a<61 )
		{
			System.out.println(a +" is between 41 to 60");
		}
		
		
	}
	
}
