package basickeywords;

public class NonPrimitiveDataType {

	
	public static void main(String[] args) {
		
		String s = "Velocity";
		
		System.out.println(s);//Velocity
		
//		this is morning batch
		System.out.println(s+"    corporate");//Velocity   corporate
		
		String s1 = "Training";
		
		System.out.println(s+" "+"corporate "+" "+s1);//Velocity corporate Training
		
		
		int i = 50;
		
		
		System.out.println(i+"a");//50a
		
		
		System.out.println(i+"1");//501
		
		
		System.out.println(20+2+"5");//225
		
		
		System.out.println(20+2+"5"+2);//2252
		
		
		System.out.println(40+40+"abc"+20+20);//80abc2020
		
		
		
	
		
		
		
		
	}
	
}
