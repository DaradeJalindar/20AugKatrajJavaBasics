package basickeywords;

public class NonnumericDataType {
	
	
	public static void main(String[] args) {
		
			char c = 'u';
			
			char d = '!';
			
			char value = '4';
			
		System.out.println(c);
		
		System.out.println(d);
			
		System.out.println(value);
		
		
		boolean b = true;
		
		boolean g = false;
		
		
		System.out.println(b);
		
		System.out.println(g);
		
		
		
		
	}

}
