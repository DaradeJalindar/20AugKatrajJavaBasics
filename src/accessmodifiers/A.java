public class A {
	
	protected String g = "Pune";
	
	protected static int o = 80;
	
	protected void m1()
	{
		System.out.println("protected method m1 from A class");
	}
	
	protected static void m2()
	{
		System.out.println("protected static method m2 from A class");
	}
	
	

}
