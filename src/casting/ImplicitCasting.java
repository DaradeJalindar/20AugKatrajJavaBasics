package casting;

public class ImplicitCasting {
	
	public static void main(String[] args) {
		
		
		byte b = 50;
		
		int c = (int)b;
		
		System.out.println(c);//50
		
		
		int d = 70;
		
		long e = (long)d;
		
		System.out.println(e);//70
		
		
		char t = 'a';
		
		int i = (int)t;
	
		System.out.println(i);//97
		
		
		
		
		
		
		
	}

}
