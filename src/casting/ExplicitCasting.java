package casting;

public class ExplicitCasting {
	
	
	
	public static void main(String[] args) {
		
		float f = 89.23f;
		
		short d = (short)f;
		
		System.out.println(d);//89
		
		
		
		int i = 131;
		
		byte b = (byte)i;
	
		System.out.println(b);//-125
		
		
		short s = 15;
		
		byte g = (byte)s;
		
		System.out.println(g);
		
		
		
		
		
	}

}
